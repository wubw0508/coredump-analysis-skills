SUMMARY_DIR_NAME = "6.总结报告"


def parse_args():
    parser = argparse.ArgumentParser(description="Generate workspace-level coredump analysis summaries")
    parser.add_argument("--workspace", required=True)
    parser.add_argument("--packages", help="Comma separated package list")
    parser.add_argument("--date-range-label", default="")
    parser.add_argument("--status-file", help="Package status TSV generated by run_analysis_agent.sh")
    parser.add_argument("--version-status-file", help="Version status TSV generated by analyze_crash_complete.sh")
    parser.add_argument("--failed-packages", default="", help="Comma separated failed package list")
    parser.add_argument("--list-failed", action="store_true", help="Print packages that should be retried")
    return parser.parse_args()


def read_json(path):
    try:
        with open(path, "r", encoding="utf-8") as f:
            return json.load(f)
    except Exception:
        return None


def write_json(path, data):
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2, ensure_ascii=False)


def split_packages(value):
    return [p.strip() for p in (value or "").split(",") if p.strip()]


def load_run_context(summary_dir):
    return read_json(summary_dir / "run_context.json") or {}


def latest_status_by_package(status_file):
    statuses = {}
    if not status_file:
        return statuses
    path = Path(status_file)
    if not path.exists():
        return statuses
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.rstrip("\n")
            if not line or line.startswith("#"):
                continue
            parts = line.split("\t", 4)
            if len(parts) < 4:
                continue
            timestamp, package, status, exit_code = parts[:4]
            message = parts[4] if len(parts) > 4 else ""
            statuses[package] = {
                "timestamp": timestamp,
                "status": status,
                "exit_code": exit_code,
                "message": message,
            }
    return statuses


def load_version_status(version_status_file):
    result = defaultdict(list)
    if not version_status_file:
        return result
    path = Path(version_status_file)
    if not path.exists():
        return result
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.rstrip("\n")
            if not line or line.startswith("#"):
                continue
            parts = line.split("\t", 5)
            if len(parts) < 5:
                continue
            timestamp, package, version, step, status = parts[:5]
            message = parts[5] if len(parts) > 5 else ""
            result[package].append({
                "timestamp": timestamp,
                "version": version,
                "step": step,
                "status": status,
                "message": message,
            })
    return result


def discover_packages(workspace, requested):
    packages = set(requested)
    for stats in (workspace / "2.数据筛选").glob("*_crash_statistics.json"):
        packages.add(stats.name.replace("_crash_statistics.json", ""))
    analysis_root = workspace / "5.崩溃分析"
    if analysis_root.exists():
        for child in analysis_root.iterdir():
            if child.is_dir():
                packages.add(child.name)
    return sorted(packages)


def stats_summary(workspace, package):
    stats_path = workspace / "2.数据筛选" / f"{package}_crash_statistics.json"
    data = read_json(stats_path) or {}
    summary = data.get("summary", {})
    return stats_path, {
        "total_records": int(summary.get("total_records", 0) or 0),
        "valid_records": int(summary.get("valid_records", 0) or 0),
        "unique_crashes": int(summary.get("unique_crashes", 0) or 0),
        "duplicate_crashes": int(summary.get("duplicate_crashes", 0) or 0),
        "versions_count": int(summary.get("versions_count", 0) or 0),
        "analysis_time": summary.get("analysis_time", ""),
        "by_signal": data.get("by_signal", {}),
        "by_version": data.get("by_version", {}),
    }


def report_paths(workspace, package):
    analysis_dir = workspace / "5.崩溃分析" / package
    summary_dir = workspace / SUMMARY_DIR_NAME
    return {
        "analysis_dir": str(analysis_dir),
        "full_analysis_report": str(analysis_dir / "full_analysis_report.md"),
        "ai_analysis_report": str(analysis_dir / "AI_analysis_report.md"),
        "package_final_conclusion": str(summary_dir / "final_conclusion.md"),
        "package_summary_statistics": str(summary_dir / "summary_statistics.json"),
    }


def infer_package_status(stats, analysis_count, source_available, full_report, ai_report, event):
    if event and event.get("status") == "failed":
        return "failed"
    if event and event.get("status") == "running":
        return "running"
    if event and event.get("status") == "skipped_completed":
        return "skipped_completed"

    if stats["valid_records"] == 0 and (full_report.exists() or ai_report.exists()):
        return "completed_no_data"
    if full_report.exists() and ai_report.exists():
        if stats["valid_records"] > 0 and stats["versions_count"] > 0 and not source_available:
            return "completed_data_only"
        if stats["versions_count"] > 0 and analysis_count < stats["versions_count"]:
            return "partial"
        return "completed"
    if stats["valid_records"] > 0 or analysis_count > 0:
        return "partial"
    return "pending"


def package_entry(workspace, package, event, version_events):
    stats_path, stats = stats_summary(workspace, package)
    paths = report_paths(workspace, package)
    analysis_dir = Path(paths["analysis_dir"])
    version_dirs = list(analysis_dir.glob("version_*")) if analysis_dir.exists() else []
    analysis_count = sum(1 for d in version_dirs if (d / "analysis.json").exists())
    source_available = (workspace / "3.代码管理" / package / ".git").exists()
    deb_count = len(list((workspace / "4.包管理" / "downloads").glob(f"{package}*.deb")))
    full_report = Path(paths["full_analysis_report"])
    ai_report = Path(paths["ai_analysis_report"])

    source_failures = sum(1 for e in version_events if e["step"] == "source" and e["status"] != "ok")
    package_skips = sum(1 for e in version_events if e["step"] == "package" and e["status"].startswith("skipped"))
    package_failures = sum(1 for e in version_events if e["step"] == "package" and e["status"].startswith("failed"))
    analysis_failures = sum(1 for e in version_events if e["step"] == "analysis" and e["status"] != "ok")

    status = infer_package_status(stats, analysis_count, source_available, full_report, ai_report, event)
    return {
        "package": package,
        "status": status,
        "last_event": event or {},
        "statistics_file": str(stats_path),
        "total_records": stats["total_records"],
        "valid_records": stats["valid_records"],
        "unique_crashes": stats["unique_crashes"],
        "duplicate_crashes": stats["duplicate_crashes"],
        "versions_count": stats["versions_count"],
        "analyzed_versions": analysis_count,
        "source_available": source_available,
        "source_failures": source_failures,
        "package_skips": package_skips,
        "package_failures": package_failures,
        "analysis_failures": analysis_failures,
        "downloaded_deb_files": deb_count,
        "reports": {
            "full_analysis_report_exists": full_report.exists(),
            "ai_analysis_report_exists": ai_report.exists(),
            **paths,
        },
    }


def categorize_root_cause(text):
    lower = text.lower()
    if any(x in lower for x in ("dbus", "qdbus", "pendingcall", "watcher")):
        return "D-Bus连接/回调生命周期"
    if any(x in lower for x in ("xcb", "x11", "display", "qxcb")):
        return "X11/XCB连接"
    if any(x in lower for x in ("qobject", "qwidget", "postevent", "event", "delete", "destroy", "d1ev", "d0ev")):
        return "Qt对象生命周期/事件循环"
    if any(x in lower for x in ("thread", "qtconcurrent", "runfunctiontask", "pthread")):
        return "线程/并发访问"
    if any(x in lower for x in ("assert", "abort", "raise", "sigabrt")):
        return "断言/主动终止"
    if any(x in lower for x in ("null", "segv", "sigsegv", "invalid", "pointer")):
        return "内存访问/空指针"
    return "待人工判断"


def crash_key(crash):
    symbol = crash.get("app_layer_symbol") or ""
    key_frame = crash.get("key_frame") or {}
    if not symbol:
        symbol = key_frame.get("symbol") or ""
    if not symbol:
        frames = crash.get("frames") or []
        if frames:
            symbol = frames[0].get("symbol", "")
    if not symbol:
        symbol = "unknown"
    symbol = re.sub(r"\s+", " ", symbol).strip()
    signal = crash.get("signal") or "UNKNOWN"
    library = crash.get("app_layer_library") or key_frame.get("library") or ""
    return f"{signal}|{library}|{symbol}"


def collect_clusters(workspace, packages):
    clusters = {}
    for package in packages:
        for analysis_file in sorted((workspace / "5.崩溃分析" / package).glob("version_*/analysis.json")):
            data = read_json(analysis_file) or {}
            version = data.get("version") or analysis_file.parent.name.replace("version_", "").replace("_", ".")
            for crash in data.get("crashes", []):
                key = crash_key(crash)
                count = int(crash.get("count", 1) or 1)
                text = " ".join([
                    key,
                    crash.get("description", ""),
                    crash.get("fix_reason", ""),
                    crash.get("stack_info", "")[:500],
                ])
                item = clusters.setdefault(key, {
                    "cluster_id": f"C{len(clusters) + 1:04d}",
                    "key": key,
                    "root_cause_category": categorize_root_cause(text),
                    "total_count": 0,
                    "unique_crashes": 0,
                    "packages": set(),
                    "versions": set(),
                    "signals": set(),
                    "example": crash,
                })
                item["total_count"] += count
                item["unique_crashes"] += 1
                item["packages"].add(package)
                item["versions"].add(f"{package}:{version}")
                item["signals"].add(crash.get("signal") or "UNKNOWN")

    result = []
    for item in clusters.values():
        item["packages"] = sorted(item["packages"])
        item["versions"] = sorted(item["versions"])
        item["signals"] = sorted(item["signals"])
        result.append(item)
    result.sort(key=lambda x: (-x["total_count"], -x["unique_crashes"], x["key"]))
    return result


def clean_version_label(value):
    value = (value or "").strip()
    if not value:
        return ""
    if ":" in value:
        value = value.split(":", 1)[1]
    value = re.sub(r"-1$", "", value)
    return value


def load_expected_versions(workspace, package):
    path = workspace / "2.数据筛选" / f"{package}_crash_versions.txt"
    versions = []
    if not path.exists():
        return versions
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            rest = line.rsplit(":", 1)[0]
            versions.append(clean_version_label(rest))
    return versions


def build_retry_targets(workspace, manifest, version_statuses, run_context):
    retry_package_statuses = {"failed", "partial", "running", "pending"}
    retry_packages = []
    retry_versions = []

    for entry in manifest["packages"]:
        package = entry["package"]
        if entry["status"] in retry_package_statuses:
            retry_packages.append(package)

        events = version_statuses.get(package, [])
        by_version = defaultdict(list)
        for event in events:
            version = clean_version_label(event.get("version", ""))
            if version:
                by_version[version].append(event)

        expected_versions = load_expected_versions(workspace, package)
        for version in expected_versions:
            version_events = by_version.get(version, [])
            step_status = {e["step"]: e["status"] for e in version_events}
            failed_steps = [e["step"] for e in version_events if e["status"] not in ("ok", "skipped_no_sudo")]
            last_message = version_events[-1]["message"] if version_events else "no execution record"
            needs_retry = False

            if not version_events:
                needs_retry = True
                failed_steps = ["missing"]
            elif step_status.get("analysis") != "ok":
                needs_retry = True
            elif step_status.get("source", "").startswith("failed"):
                needs_retry = True
            elif any(status.startswith("failed") for status in step_status.values()):
                needs_retry = True

            if needs_retry:
                retry_strategy, step_retry_command = build_step_retry_strategy(
                    package, version, failed_steps, workspace, run_context
                )
                retry_versions.append({
                    "package": package,
                    "version": version,
                    "failed_steps": failed_steps,
                    "last_message": last_message,
                    "command": build_retry_command(package, workspace, run_context, version),
                    "retry_strategy": retry_strategy,
                    "step_command": step_retry_command,
                })

    retry_packages = sorted(set(retry_packages))
    retry_versions.sort(key=lambda x: (x["package"], x["version"]))
    return retry_packages, retry_versions


def build_retry_command(package, workspace, run_context, version=None):
    parts = [
        "bash coredump-full-analysis/scripts/analyze_crash_complete.sh" if version else "bash run_analysis_agent.sh",
        f"--package {package}" if version else f"--packages {package}",
        f"--workspace {workspace}",
    ]
    arch = run_context.get("arch", "")
    sys_version = run_context.get("sys_version", "")
    start_date = run_context.get("start_date", "")
    end_date = run_context.get("end_date", "")
    if arch:
        parts.append(f"--arch {arch}")
    if sys_version:
        parts.append(f"--sys-version {sys_version}")
    if start_date:
        parts.append(f"--start-date {start_date}")
    if end_date:
        parts.append(f"--end-date {end_date}")
    if version:
        parts.append(f"--versions {version}")
    return " ".join(parts)


def build_retry_step_command(package, version, workspace, run_context, strategy):
    parts = [
        "bash coredump-full-analysis/scripts/run_retry_step.sh",
        f"--package {package}",
        f"--version {version}",
        f"--workspace {workspace}",
        f"--strategy {strategy}",
    ]
    arch = run_context.get("arch", "")
    sys_version = run_context.get("sys_version", "")
    start_date = run_context.get("start_date", "")
    end_date = run_context.get("end_date", "")
    if arch:
        parts.append(f"--arch {arch}")
    if sys_version:
        parts.append(f"--sys-version {sys_version}")
    if start_date:
        parts.append(f"--start-date {start_date}")
    if end_date:
        parts.append(f"--end-date {end_date}")
    return " ".join(parts)


def build_step_retry_strategy(package, version, failed_steps, workspace, run_context):
    normalized_steps = [step for step in failed_steps if step]
    if "missing" in normalized_steps or "source" in normalized_steps:
        return "full_version_rerun", build_retry_step_command(package, version, workspace, run_context, "full_version_rerun")

    if not normalized_steps:
        return "full_version_rerun", build_retry_step_command(package, version, workspace, run_context, "full_version_rerun")

    if normalized_steps == ["analysis"]:
        return "analysis_only", build_retry_step_command(package, version, workspace, run_context, "analysis_only")

    if set(normalized_steps).issubset({"package", "analysis"}):
        return "package_then_analysis", build_retry_step_command(package, version, workspace, run_context, "package_then_analysis")

    return "full_version_rerun", build_retry_step_command(package, version, workspace, run_context, "full_version_rerun")


def generate_manifest(workspace, packages, package_statuses, version_statuses, date_range_label):
    entries = [
        package_entry(workspace, package, package_statuses.get(package), version_statuses.get(package, []))
        for package in packages
    ]
    totals = {
        "packages": len(entries),
        "completed": sum(1 for e in entries if e["status"] in ("completed", "completed_no_data", "completed_data_only", "skipped_completed")),
        "failed": sum(1 for e in entries if e["status"] == "failed"),
        "partial": sum(1 for e in entries if e["status"] == "partial"),
        "pending": sum(1 for e in entries if e["status"] == "pending"),
        "valid_records": sum(e["valid_records"] for e in entries),
        "unique_crashes": sum(e["unique_crashes"] for e in entries),
        "versions": sum(e["versions_count"] for e in entries),
        "analyzed_versions": sum(e["analyzed_versions"] for e in entries),
    }
    return {
        "generated_at": datetime.now().isoformat(timespec="seconds"),
        "workspace": str(workspace),
        "summary_dir": str(workspace / SUMMARY_DIR_NAME),
        "date_range": date_range_label,
        "totals": totals,
        "packages": entries,
    }


