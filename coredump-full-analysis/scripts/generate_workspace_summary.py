def build_summary_refresh_block(run_context):
    packages = run_context.get("packages", "")
    date_range_label = run_context.get("date_range_label", "")
    packages_arg = json.dumps(packages, ensure_ascii=False)
    date_range_arg = json.dumps(date_range_label, ensure_ascii=False)
    return [
        "\n# 刷新 workspace 汇总\n",
        "python3 coredump-full-analysis/scripts/generate_workspace_summary.py \\\n",
        "  --workspace \"$WORKSPACE\" \\\n",
        f"  --packages {packages_arg} \\\n",
        f"  --date-range-label {date_range_arg}\n",
    ]


def write_retry_summary_md(path, manifest, retry_packages, retry_versions):
    package_map = {entry["package"]: entry for entry in manifest["packages"]}
    retry_by_package = defaultdict(list)
    for item in retry_versions:
        retry_by_package[item["package"]].append(item)

    lines = [
        "# 重跑摘要\n\n",
        f"**生成时间**: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n\n",
        f"- 需要重跑的包: {len(retry_packages)}\n",
        f"- 需要重跑的版本: {len(retry_versions)}\n\n",
    ]

    if retry_packages:
        lines.extend([
            "## 包级重跑\n\n",
            "| 包 | 当前状态 | 最近结果 | 有效记录 | 唯一崩溃 | 说明 |\n",
            "|----|----------|----------|----------|----------|------|\n",
        ])
        for package in retry_packages:
            entry = package_map.get(package, {})
            last_event = entry.get("last_event", {}) if entry else {}
            message = last_event.get("message", "") or "reported by manifest"
            lines.append(
                f"| {package} | {entry.get('status', 'unknown')} | {last_event.get('status', 'unknown')} | "
                f"{entry.get('valid_records', 0)} | {entry.get('unique_crashes', 0)} | {message} |\n"
            )
        lines.append("\n")

    if retry_versions:
        lines.extend([
            "## 版本级重跑\n\n",
            "| 包 | 版本 | 失败步骤 | 最后信息 |\n",
            "|----|------|----------|----------|\n",
        ])
        for item in retry_versions:
            lines.append(
                f"| {item['package']} | {item['version']} | {', '.join(item['failed_steps'])} | "
                f"{item['last_message']} |\n"
            )
        lines.append("\n")

        lines.append("## 按包汇总\n\n")
        for package in sorted(retry_by_package):
            lines.append(f"### {package}\n\n")
            for item in retry_by_package[package]:
                lines.append(
                    f"- 版本 `{item['version']}`: 步骤 `{', '.join(item['failed_steps'])}`; "
                    f"策略 `{item['retry_strategy']}`; {item['last_message']}\n"
                )
            lines.append("\n")
    else:
        lines.append("## 版本级重跑\n\n当前没有需要精确重跑的版本。\n")

    lines.extend([
        "## 相关文件\n\n",
        f"- `retry_packages.txt`\n",
        f"- `retry_versions.tsv`\n",
        f"- `retry_commands.sh`\n",
        f"- `retry_versions.sh`\n",
        f"- `retry_failed_steps.sh`\n",
        f"- `validate_workspace_retry_closure.py`\n",
        f"- `validate_workspace.sh`\n",
    ])
    path.write_text("".join(lines), encoding="utf-8")


def main():
    args = parse_args()
    workspace = Path(args.workspace)
    summary_dir = workspace / SUMMARY_DIR_NAME
    summary_dir.mkdir(parents=True, exist_ok=True)
    run_context = load_run_context(summary_dir)

    requested = split_packages(args.packages)
    packages = discover_packages(workspace, requested)
    package_statuses = latest_status_by_package(args.status_file or str(summary_dir / "package_status.tsv"))
    version_statuses = load_version_status(args.version_status_file or str(summary_dir / "version_status.tsv"))

    for pkg in split_packages(args.failed_packages):
        package_statuses[pkg] = {
            "timestamp": datetime.now().isoformat(timespec="seconds"),
            "status": "failed",
            "exit_code": "1",
            "message": "reported by caller",
        }

    manifest = generate_manifest(workspace, packages, package_statuses, version_statuses, args.date_range_label)
    clusters = collect_clusters(workspace, packages)
    retry_packages, retry_versions = build_retry_targets(workspace, manifest, version_statuses, run_context)

    if args.list_failed:
        retry_statuses = {"failed", "partial", "running", "pending"}
        for entry in manifest["packages"]:
            if entry["status"] in retry_statuses:
                print(entry["package"])
        return

    write_json(summary_dir / "run_manifest.json", manifest)
    write_manifest_md(summary_dir / "run_manifest.md", manifest)
    write_json(summary_dir / "all_packages_summary.json", {
        "generated_at": manifest["generated_at"],
        "workspace": manifest["workspace"],
        "date_range": manifest["date_range"],
        "totals": manifest["totals"],
        "packages": manifest["packages"],
    })
    write_all_summary(summary_dir / "all_packages_summary.md", manifest)
    write_json(summary_dir / "root_cause_clusters.json", clusters)
    write_cluster_md(summary_dir / "root_cause_clusters.md", clusters)
    write_retry_packages(summary_dir / "retry_packages.txt", retry_packages)
    write_json(summary_dir / "retry_versions.json", retry_versions)
    write_retry_versions_tsv(summary_dir / "retry_versions.tsv", retry_versions)
    write_retry_versions_md(summary_dir / "retry_versions.md", retry_versions)
    write_retry_commands(summary_dir / "retry_commands.sh", workspace, retry_packages, run_context)
    write_retry_version_commands(summary_dir / "retry_versions.sh", workspace, retry_versions, run_context)
    write_retry_failed_step_commands(summary_dir / "retry_failed_steps.sh", workspace, retry_versions, run_context)
    write_retry_summary_md(summary_dir / "retry_summary.md", manifest, retry_packages, retry_versions)

    print(f"✅ run_manifest.md 已生成: {summary_dir / 'run_manifest.md'}")
    print(f"✅ all_packages_summary.md 已生成: {summary_dir / 'all_packages_summary.md'}")
    print(f"✅ root_cause_clusters.md 已生成: {summary_dir / 'root_cause_clusters.md'}")
    print(f"✅ retry_packages.txt 已生成: {summary_dir / 'retry_packages.txt'}")
    print(f"✅ retry_versions.md 已生成: {summary_dir / 'retry_versions.md'}")
    print(f"✅ retry_versions.sh 已生成: {summary_dir / 'retry_versions.sh'}")
    print(f"✅ retry_failed_steps.sh 已生成: {summary_dir / 'retry_failed_steps.sh'}")
    print(f"✅ retry_summary.md 已生成: {summary_dir / 'retry_summary.md'}")


if __name__ == "__main__":
    main()
