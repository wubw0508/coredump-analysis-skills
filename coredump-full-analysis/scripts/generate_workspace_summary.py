def write_retry_version_commands(path, workspace, retry_versions, run_context):
    summary_dir_literal = json.dumps(str(workspace / SUMMARY_DIR_NAME), ensure_ascii=False)
    lines = [
        "#!/bin/bash\n",
        "set -e\n\n",
        f"WORKSPACE={json.dumps(str(workspace))}\n\n",
        f"SUMMARY_DIR={summary_dir_literal}\n",
        "TARGET_VERSIONS_FILE=\"$SUMMARY_DIR/.retry_versions_targets.tsv\"\n\n",
    ]
    if retry_versions:
        lines.append("cat > \"$TARGET_VERSIONS_FILE\" <<'EOF'\n")
        for item in retry_versions:
            lines.append(f"{item['package']}\t{item['version']}\n")
        lines.append("EOF\n\n")
    for item in retry_versions:
        command = build_retry_command(item["package"], "$WORKSPACE", run_context, item["version"])
        lines.append(f"{command}\n")
    lines.extend(build_summary_refresh_block(run_context))
    if retry_versions:
        lines.extend([
            "\n# 校验版本级重跑结果\n",
            "python3 coredump-full-analysis/scripts/verify_retry_targets.py \\\n",
            "  --summary-dir \"$SUMMARY_DIR\" \\\n",
            "  --versions-file \"$TARGET_VERSIONS_FILE\"\n",
        ])
    path.write_text("".join(lines), encoding="utf-8")
    path.chmod(0o755)


def write_retry_failed_step_commands(path, workspace, retry_versions, run_context):
    summary_dir_literal = json.dumps(str(workspace / SUMMARY_DIR_NAME), ensure_ascii=False)
